    private void btnSaveClassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveClassActionPerformed
    String subject = txtSubject.getText().trim();
    String day = txtDate.getText().trim();
    String time = txtTime.getText().trim();
    String location = txtLocation.getText().trim();

    if (subject.isEmpty() || day.isEmpty() || time.isEmpty() || location.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.");
        return;
    }

    // Example class ID generation (random or increment later)
    int classID = (int)(Math.random() * 10000);  // Replace with better logic
    int tutorID = 1; // Replace with actual logged-in tutor ID
    int studentID = 0; // Not assigned now

    ClassSchedule newClass = new ClassSchedule(studentID, tutorID, classID, subject, day, time, location);

    try (BufferedWriter bw = new BufferedWriter(new FileWriter("schedule.txt", true))) {
        bw.write(studentID + "," + tutorID + "," + classID + "," + subject + "," + day + "," + time + "," + location);
        bw.newLine();
        JOptionPane.showMessageDialog(this, "Class added successfully!");
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error saving class.");
        e.printStackTrace();
    }

    // Clear fields
    txtSubject.setText("");
    txtDate.setText("");
    txtTime.setText("");
    txtLocation.setText("");
    }//GEN-LAST:event_btnSaveClassActionPerformed

